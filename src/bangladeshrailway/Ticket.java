    public String inTotal(int totalSit, int price) {
        double total = totalSit * price;
        total = total + (total * .15);
        String Price = String.valueOf(total);
        showText.setText("Total price is " + Price + " 0 Tk only (+15% vat)");
        return Price;
    }

