    private void printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionPerformed

        String value = (String) totalSit.getSelectedItem();
        String sit = (String) sitClass.getSelectedItem();
        String jFrom = (String) jComboBox2.getSelectedItem();
        String jTo = (String) jComboBox3.getSelectedItem();
        String Time = (String) time.getSelectedItem();
        String bDate = ((JTextField) date.getDateEditor().getUiComponent()).getText();
        String userName = name.getText();
        String userEmail = email.getText();
        String userPhone = phone.getText();
        String userTicket1 = ticketNo1.getText();
        String userTicket2 = ticketNo2.getText();
        String userTicket3 = ticketNo3.getText();

        String textField = area.getText();
        if (textField.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fillup the form first");
        } else {

            try {
                area.print();

            } catch (PrinterException ex) {
                Logger.getLogger(Ticket.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        PreparedStatement ps;

        String quary = "INSERT INTO `ticket`(`name`, `email`, `phone`, `date`, `time`, `from`, `to`, `totalsit`, `classdetails`, `ticket1`, `ticket2`, `ticket3`) VALUES (?,?,?,?,?,?,?,?,?,?,?,?)";

        try {
            ps = ConnectToServer.getConnection().prepareCall(quary);

            ps.setString(1, userName);
            ps.setString(2, userEmail);
            ps.setString(3, userPhone);
            ps.setString(4, bDate);
            ps.setString(5, Time);
            ps.setString(6, jFrom);
            ps.setString(7, jTo);
            ps.setString(8, value);
            ps.setString(9, sit);
            ps.setString(10, userTicket1);
            ps.setString(11, userTicket2);
            ps.setString(12, userTicket3);

            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Ticket confarmed");

            }

        } catch (SQLException ex) {
            Logger.getLogger(Signup.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_printActionPerformed

